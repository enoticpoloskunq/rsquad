// Raccoon colors - neon purple/cyan style
val Purple80 = Color(0xFFD0BCFF)
val PurpleGrey80 = Color(0xFFCCC2DC)
val Pink80 = Color(0xFFEFB8C8)

val Purple40 = Color(0xFF6650a4)
val PurpleGrey40 = Color(0xFF625b71)
val Pink40 = Color(0xFF7D5260)

// Custom Raccoon palette
val RaccoonPurple = Color(0xFF7C3AED)
val RaccoonCyan = Color(0xFF06B6D4)
val RaccoonDark = Color(0xFF0F0F1A)
val RaccoonSurface = Color(0xFF1A1A2E)
val RaccoonCard = Color(0xFF16213E)

private val DarkColorScheme = darkColorScheme(
    primary = RaccoonPurple,
    secondary = RaccoonCyan,
    tertiary = Pink80,
    background = RaccoonDark,
    surface = RaccoonSurface,
    surfaceVariant = RaccoonCard,
    onPrimary = Color.White,
    onSecondary = Color.White,
    onTertiary = Color.White,
    onBackground = Color.White,
    onSurface = Color.White
)

private val LightColorScheme = lightColorScheme(
    primary = Purple40,
    secondary = PurpleGrey40,
    tertiary = Pink40,
    background = Color(0xFFFFFBFE),
    surface = Color(0xFFFFFBFE),
    onPrimary = Color.White,
    onSecondary = Color.White,
    onTertiary = Color.White,
    onBackground = Color(0xFF1C1B1F),
    onSurface = Color(0xFF1C1B1F)
)

@Composable
fun RaccoonSquadTheme(
    darkTheme: Boolean = true, // Always dark by default!
    dynamicColor: Boolean = false,
    content: @Composable () -> Unit
) {
    val colorScheme = when {
        darkTheme -> DarkColorScheme
        else -> LightColorScheme
    }
    
    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            window.statusBarColor = colorScheme.background.toArgb()
            WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = false
        }
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content
    )
}
